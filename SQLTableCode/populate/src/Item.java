public class Item {
    int ItemID;
    String ItemName;
    float Price;
    int InventoryCount;

    public Item(int ItemID, String ItemName, float Price, int InventoryCount) {
        this.ItemID = ItemID;
        this.ItemName = ItemName;
        this.Price = Price;
        this.InventoryCount = InventoryCount;
    }
}
